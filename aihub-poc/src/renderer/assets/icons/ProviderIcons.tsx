// Color palette for letter-based icons
const LETTER_COLORS = [
  '#E74C3C', // red
  '#3498DB', // blue
  '#2ECC71', // green
  '#F39C12', // orange
  '#9B59B6', // purple
  '#1ABC9C', // teal
  '#E91E63', // pink
  '#00BCD4', // cyan
  '#FF5722', // deep orange
  '#607D8B'  // blue grey
]

function getLetterColor(name: string): string {
  let hash = 0
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash)
  }
  return LETTER_COLORS[Math.abs(hash) % LETTER_COLORS.length]
}

interface ProviderIconProps {
  providerId: string
  name?: string
  size?: number
}

export function ProviderIcon({ providerId, name, size = 30 }: ProviderIconProps): React.ReactElement {
  const IconComponent = iconMap[providerId]

  if (IconComponent) {
    return <IconComponent size={size} />
  }

  // Fallback: generate a letter icon using provider name's first letter
  const displayName = name || providerId
  const letter = displayName.charAt(0).toUpperCase()
  const bgColor = getLetterColor(displayName)

  return (
    <svg width={size} height={size} viewBox="0 0 30 30">
      <rect width="30" height="30" rx="7" fill={bgColor} />
      <text
        x="15"
        y="20"
        textAnchor="middle"
        fill="white"
        fontSize="16"
        fontWeight="bold"
        fontFamily="system-ui"
      >
        {letter}
      </text>
    </svg>
  )
}
